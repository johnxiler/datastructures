# Dictionaries: Dictionaries are unordered collections of items that are stored as key-value pairs. They are defined using curly braces and keys are separated from values by a colon.

# Define a dictionary with string keys and integer values
my_dict = {'one': 1, 'two': 2, 'three': 3}

# Define a dictionary with integer keys and string values
my_dict = {1: 'apple', 2: 'banana', 3: 'cherry'}

# Define a dictionary with mixed data types
my_dict = {'name': 'John', 'age': 30, 'employed': True}

# Access values in a dictionary
print(my_dict['name'])  # John
print(my_dict['age'])  # 30
